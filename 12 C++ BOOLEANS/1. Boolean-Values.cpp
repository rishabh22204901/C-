// C++ Booleans

// very often, in c programming, We will need a data type that have only one or two values, like:

// 1. YES / NO
// 2. ON / OFF
// 3. TRUE / FALSE

// For this, C++ has a bool data type, which can take the values true (1) or false (0)


#include <iostream>
using namespace std;
int main(){
    bool isCodingFun = true;
    bool isFishTasty = false;

    cout << isCodingFun; // 1 (true)
    cout << isFishTasty; // 0 (false)

}