// Variables are containers for storing data values.

// In C++, there are different types of variables (defined with different keywords), for example:

// a. int - stores integers (whole numbers), without decimals, such as 123 or -123
// b. double - stores floating point numbers, with decimals, such as 19.99 or -19.99
// c. char - stores single characters, such as 'a' or 'B'. Char values are surrounded by single quotes
// d. string - stores text, such as "Hello World". String values are surrounded by double quotes
// e. bool - stores values with two states: true or false

