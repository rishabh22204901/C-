// To get the length of a string, use the length() function:

#include <iostream>
using namespace std;
int main(){
    string text = "My name is rishabh";
    cout << "The length of the string text is : " << text.length() << "\n";

    // some C++ programs that use the size() function to get the length of a string. This is just an alias of length(). It is completely up to you if you want to use length() or size():

    cout << "The length of the string text is : " << text.size();
    

    
}